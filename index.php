<p>This ecosystem enables enterprises and service providers to deploy strong authentication solutions that reduce reliance on passwords and protect against phishing, man-in-the-middle and replay attacks using stolen passwords.</br>
As such, Wega’s acoustic chip, 3DSA 2.0 servers and Pin Code have all been <a href="https://fidoalliance.org/certification/fido-certified-products/">FIDO Certified</a>
Fido ignites a thriving ecosystem of client authentication methods such as biometrics, PINs and second–factors that can be used with a variety of online services in an interoperable manner. To complete its operating process, Wega has selected qualified FIDO certified biometrics authenticators in the 3DSA 2.0 platform and has integrated then within its plateform and its Wega mobile app.</p>
