                                            <h3>About FIDO Alliance and FIDO standards</h3>
                                            <p>The Fast Identity Online (FIDO) Alliance was formed in 2013 to revolutionize online authentication by developing open, interoperable industry standards that leverage proven public key cryptography for stronger security and device-based user verification for better usability. Through the collaborative efforts of FIDO, new standards and specifications have emerged that enable strong, easy-to-use authentication to be built into devices.</br>
In a typical deployment of these standards, a user swipes a finger, speaks a phrase, looks at a camera on a device to login, pay for an item, or use another service.</br>
The biometric verification is used as an initial factor to then unlock a second, more secure factor. Since biometrics and cryptographic keys are stored on local devices and never sent across the network – eliminating shared secrets – user credentials are secure even if service providers get hacked, thereby eliminating the possibility of scalable data breaches.</br> 
FIDO standards are currently being used to enable simpler, stronger authentication in offerings from Google, PayPal, Bank of America, NTT DOCOMO, BC Card (Korea), Microsoft, Dropbox, GitHub, AliPay, eBay, Samsung, Facebook, and other leading firms. In each of these deployments, end-users do not have to know how the authentication works or why it’s more secure – they are getting login experiences that are easier to use, with great security baked in behind the scenes.</br>
The FIDO approach has been embraced by the World Wide Web Consortium (W3C), which is expected to finalize a formal new “Web Authentication” standard built on FIDO specifications in 2017. </p>
<p>FIDO Alliance was selected by EMVCo for the use of FIDO concepts and requirements in EMVCo 3DS 2.0 specifications for multi-factor Strong Authentication solutions with the following objectives:</p>

<p>
- Reduce card fraud</br>
- Maintain convenient user experience</br>
- Greatly simplify the development and support for CDCVM across mobile devices and other platforms</br>
- Provide a standard way for mobile wallet providers and payment application developers to support Consumer Device Cardholder Verification Methods (CDCVM)</p>
