                                            <p>While needing to access any account functionalities , the user choses to place its Wega card in front of the microphone and presses the button on the card. An audio sequence is issued by the card and then transmitted to the interactive server. This first step is used to identify the user and the holder of the card. No EMV related information are passed during this sequence. There are no technical relationship between the acoustic Chip and the EMV elements. The audio sequence only contains dynamic OTPs (One-Time-Password) and a unique token reference.</p>
                                            
